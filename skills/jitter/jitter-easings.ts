/**
 * Get the easing curve for a Jitter easing name and intensity.
 * Intensity is snapped to the nearest available value (0, 25, 50, 75, 100).
 */
export function getJitterEasing(name: JitterEasingName, intensity: number): JitterCurve {
  const snapped = (Math.round(intensity / 25) * 25) as JitterIntensity
  return CATALOG[name][snapped]
}

/**
 * Linearly interpolate a sampled curve at time t (0-1).
 * For bezier curves, use Remotion's Easing.bezier() instead.
 */
export function lerpSamples(samples: number[], t: number): number {
  const clamped = Math.max(0, Math.min(1, t))
  const idx = clamped * (samples.length - 1)
  const lo = Math.floor(idx)
  const hi = Math.min(lo + 1, samples.length - 1)
  const frac = idx - lo
  return samples[lo] * (1 - frac) + samples[hi] * frac
}